#!/usr/bin/env python3
"""
plot_ompl_tour_paths.py

Visualize the 4-room environment (AABB walls/obstacles) and compare
tour paths generated by different OMPL planners in ONE run (same run id).

Expected files (from Solution 1):
  results/four_rooms_boxes_env<env>.csv
  results/path_<PLANNER>_env<env>_run<run>_seed<seed>.csv

Example:
  python plot_ompl_tour_paths.py --outdir results --env 0 --run 0

Options:
  --mode xy        : top-down 2D (recommended)
  --mode xz / yz   : side views
  --mode 3d        : 3D plot
  --planners ...   : subset planners to plot
"""

import argparse
import glob
import os
import re
from typing import Dict, List

import pandas as pd
import matplotlib.pyplot as plt


DEFAULT_PLANNERS = ["RRTConnect", "PRMstar", "RRTstar", "BITstar"]


def load_boxes(boxes_csv: str) -> pd.DataFrame:
    boxes = pd.read_csv(boxes_csv)
    required = {"xmin", "xmax", "ymin", "ymax", "zmin", "zmax"}
    missing = required - set(boxes.columns)
    if missing:
        raise ValueError(f"{boxes_csv} missing columns: {sorted(missing)}")
    return boxes


def find_path_files(outdir: str, env_seed: int, run_id: int, planners: List[str]) -> Dict[str, str]:
    """
    Return planner -> pathfile for given env_seed + run_id.
    If multiple files match (different seeds), pick the lexicographically first.
    """
    res: Dict[str, str] = {}
    for p in planners:
        pat_new = os.path.join(outdir, f"path_{p}_env{env_seed}_run{run_id}_seed*.csv")
        matches = sorted(glob.glob(pat_new))
        if not matches:
            # Backward-compatibility with older naming.
            pat_old = os.path.join(outdir, f"path_{p}_run{run_id}_seed*.csv")
            matches = sorted(glob.glob(pat_old))
        if matches:
            res[p] = matches[0]
    return res


def load_path(path_csv: str) -> pd.DataFrame:
    df = pd.read_csv(path_csv)
    required = {"x", "y", "z"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"{path_csv} missing columns: {sorted(missing)}")
    return df


def extract_seed_from_filename(path: str) -> str:
    m = re.search(r"_seed(\d+)\.csv$", os.path.basename(path))
    return m.group(1) if m else "?"


def draw_boxes_2d(ax, boxes: pd.DataFrame, plane: str = "xy") -> None:
    """
    Draw AABB projections as rectangles on a chosen plane.
    plane: "xy", "xz", or "yz"
    """
    if plane == "xy":
        a0, a1, b0, b1 = "xmin", "xmax", "ymin", "ymax"
        xlabel, ylabel = "x", "y"
    elif plane == "xz":
        a0, a1, b0, b1 = "xmin", "xmax", "zmin", "zmax"
        xlabel, ylabel = "x", "z"
    elif plane == "yz":
        a0, a1, b0, b1 = "ymin", "ymax", "zmin", "zmax"
        xlabel, ylabel = "y", "z"
    else:
        raise ValueError("plane must be one of: xy, xz, yz")

    for _, b in boxes.iterrows():
        x0, x1 = float(b[a0]), float(b[a1])
        y0, y1 = float(b[b0]), float(b[b1])
        ax.plot([x0, x1, x1, x0, x0], [y0, y0, y1, y1, y0], linewidth=0.6)

    ax.set_xlabel(xlabel)
    ax.set_ylabel(ylabel)
    ax.set_aspect("equal", "box")


def plot_paths_2d(ax, paths: Dict[str, pd.DataFrame], plane: str = "xy") -> None:
    if plane == "xy":
        u, v = "x", "y"
    elif plane == "xz":
        u, v = "x", "z"
    elif plane == "yz":
        u, v = "y", "z"
    else:
        raise ValueError("plane must be one of: xy, xz, yz")

    for planner, df in paths.items():
        ax.plot(df[u], df[v], label=planner, linewidth=1.5)


def plot_3d(boxes: pd.DataFrame, paths: Dict[str, pd.DataFrame]) -> None:
    from mpl_toolkits.mplot3d import Axes3D  # noqa: F401

    fig = plt.figure()
    ax = fig.add_subplot(111, projection="3d")

    # Draw boxes as wireframes (12 edges)
    for _, b in boxes.iterrows():
        xmin, xmax = float(b["xmin"]), float(b["xmax"])
        ymin, ymax = float(b["ymin"]), float(b["ymax"])
        zmin, zmax = float(b["zmin"]), float(b["zmax"])

        # 8 corners
        corners = [
            (xmin, ymin, zmin), (xmax, ymin, zmin), (xmax, ymax, zmin), (xmin, ymax, zmin),
            (xmin, ymin, zmax), (xmax, ymin, zmax), (xmax, ymax, zmax), (xmin, ymax, zmax),
        ]
        # edges by corner indices
        edges = [
            (0, 1), (1, 2), (2, 3), (3, 0),
            (4, 5), (5, 6), (6, 7), (7, 4),
            (0, 4), (1, 5), (2, 6), (3, 7),
        ]
        for i, j in edges:
            xs = [corners[i][0], corners[j][0]]
            ys = [corners[i][1], corners[j][1]]
            zs = [corners[i][2], corners[j][2]]
            ax.plot(xs, ys, zs, linewidth=0.5)

    for planner, df in paths.items():
        ax.plot(df["x"], df["y"], df["z"], label=planner, linewidth=1.5)

    ax.set_xlabel("x")
    ax.set_ylabel("y")
    ax.set_zlabel("z")
    ax.legend()
    plt.tight_layout()


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--outdir", default="results", help="Folder with CSV outputs")
    ap.add_argument("--env", type=int, default=0, help="Environment seed id to plot")
    ap.add_argument("--run", type=int, default=0, help="Run id to plot")
    ap.add_argument("--mode", choices=["xy", "xz", "yz", "3d"], default="xy",
                    help="Plot mode: xy(top-down), xz, yz, or 3d")
    ap.add_argument("--planners", nargs="*", default=DEFAULT_PLANNERS,
                    help="Which planners to plot (default: common four)")
    ap.add_argument("--save", default="", help="If set, save figure to this path instead of showing")
    args = ap.parse_args()

    boxes_csv = os.path.join(args.outdir, f"four_rooms_boxes_env{args.env}.csv")
    if not os.path.exists(boxes_csv):
        # Backward-compatibility with older naming.
        boxes_csv = os.path.join(args.outdir, "four_rooms_boxes.csv")
    if not os.path.exists(boxes_csv):
        raise FileNotFoundError(f"Missing boxes file: {boxes_csv}")

    boxes = load_boxes(boxes_csv)

    files = find_path_files(args.outdir, args.env, args.run, args.planners)
    if not files:
        raise FileNotFoundError(
            f"No path files found for env={args.env}, run={args.run} in {args.outdir}. "
            f"Expected pattern: path_<PLANNER>_env{args.env}_run{args.run}_seed*.csv"
        )

    paths: Dict[str, pd.DataFrame] = {}
    labels_extra: Dict[str, str] = {}
    for planner, f in files.items():
        paths[planner] = load_path(f)
        labels_extra[planner] = extract_seed_from_filename(f)

    # Rename legend labels to include seed (optional but useful)
    # We'll keep legend clean but show seeds in the title
    seed_info = ", ".join([f"{p}:seed{labels_extra[p]}" for p in sorted(paths.keys())])

    if args.mode == "3d":
        plot_3d(boxes, paths)
        plt.title(f"OMPL Tour Paths (env {args.env}, run {args.run})\n{seed_info}", fontsize=10)
    else:
        fig = plt.figure()
        ax = plt.gca()
        draw_boxes_2d(ax, boxes, plane=args.mode)
        plot_paths_2d(ax, paths, plane=args.mode)
        ax.legend()
        ax.set_title(f"OMPL Tour Paths (env {args.env}, run {args.run}) [{args.mode}]\n{seed_info}", fontsize=10)
        plt.tight_layout()

    if args.save:
        plt.savefig(args.save, dpi=200)
        print(f"Saved figure to: {args.save}")
    else:
        plt.show()


if __name__ == "__main__":
    main()
